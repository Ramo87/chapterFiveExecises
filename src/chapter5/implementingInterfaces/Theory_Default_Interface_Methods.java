package chapter5.implementingInterfaces;

/**
 *                  Default Interface Methods:
 *
 *                  = a method defined in a interface as default, in which a method body is provided!
 *
 *                  = it defines an an abstract method with a default implementation
 *
 *                  = in this case, classes have the posibility to override the default method if they need to,
 *                  BUT they do NOT HAVE to do it!
 *
 *                  = if the class does not override the method, the default implementation will be used; in this manner,
 *                  the method definition is concrete not abstract!
 *
 *
 *          -> can not be marked as: as static, final or abstract! [they are allowed to be overridden but not required! ]
 *
 *          -> require an instance of the class implementing the interface to be invoked!
 *
 *          ->
 */


